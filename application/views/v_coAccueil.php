<div id="contenu">
	<h2>Gestion des frais des visiteurs</h2>
	<p>Bienvenue dans votre application de gestion des frais de déplacements.</p>
	<p>
		Afin de rembource nos visiteur vous devez confirmer leurs fiche de frais.
	</p>
		<ul>
			<li>Une fiche de frais court du 1er au dernier jour du mois;</li>
			<li>Les fiches de frais sont créées automatiquement par l'application au fil de votre utilisation;</li>
			<li>Vous complétez les fiches de frais a votre rythme;</li>
			<li>Lorsqu'une fiche est totalement renseignée, vous devrez la "signer" afin qu'elle puisse être traitée par le service comptable;</li>
		</ul>
	

	<p>
		Au moyen du bandeau gauche, vous avez accès aux fonctionalités 
		du profil comptable : 
	</p>
		<ul>
			<li>Gérer vos fiches de frais (modifier, signer, imprimer)</li>
			<li>Se déconnecter</li>
		</ul>
	
</div>